// for...of loop is used to iterate over the values of an iterable object, such as arrays, strings, maps, and sets.

const fruits = ["apple", "banana", "cherry"];

for (let fruit of fruits) {
	console.log(fruit);
}

// Output:
/*
apple
banana
cherry
*/

